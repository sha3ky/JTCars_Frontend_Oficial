export const colorsEn_Es = (value) => {
   switch (value) {
      case "red":
         return "Rojo";
      case "green":
         return "Verde";
      case "blue":
         return "Azul";
      case "black":
         return "Negro";
      case "orange":
         return "Naranja";
      case "grey":
         return "Gris";
      case "brown":
         return "Marrón";
      case "pink":
         return "Rosa";
      case "purple":
         return "Púrpura";
      default:
         return "Color Desconocido";
   }
};


