export const colorsEs_En = (value) => {
  switch (value) {
     case "Rojo":
        return "red";
     case "Verde":
        return "green";
     case "Azul":
        return "blue";
     case "Negro":
        return "black";
     case "Naranja":
        return "orange";
     case "Gris":
        return "grey";
     case "Marrón":
        return "brown";
     case "Rosa":
        return "pink";
     case "Púrpura":
        return "purple";
     default:
        return "Unknown Color";
  }
};
